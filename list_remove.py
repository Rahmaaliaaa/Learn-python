#  remove() : menghapus item yang ditentukan
#input = thislist = ["apple", "banana", "cherry"]
#Output = hapus pisang
thislist = ["apple", "banana", "cherry"]
thislist.remove("banana")
print(thislist)

# pop() : menghapus indeks yang ditentukan
# input : thislist = ["apple", "banana", "cherry"]
# Output : Hapus item kedua
thislist = ["apple", "banana", "cherry"]
thislist.pop(2)
print(thislist)